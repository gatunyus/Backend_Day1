  id       INT AUTO_INCREMENT PRIMARY KEY,
  name     VARCHAR(100)  NOT NULL,
  category VARCHAR(50)   NOT NULL,
  price    DECIMAL(10,2) NOT NULL DEFAULT 0,
  stock    INT           NOT NULL DEFAULT 0
);

